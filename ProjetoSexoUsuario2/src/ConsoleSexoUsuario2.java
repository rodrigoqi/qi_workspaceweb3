import java.util.Scanner;

public class ConsoleSexoUsuario2 {

	public static void main(String[] args) {
		Scanner leitor = new Scanner(System.in);
		
		System.out.println("SITE DA CERVEJARIA MOUG\n");
		
		System.out.println("Digite o seu nome: ");
		String nome = leitor.nextLine();
		
		System.out.println("Digite o seu sexo (F/M/I): ");
		String sexo = leitor.nextLine();
		
		if( sexo.toUpperCase().equals("F") ) {
			System.out.println("Bem-vinda " + nome.toUpperCase());
		}
		
		if( sexo.toUpperCase().equals("M") ) {
			System.out.println("Bem-vindo " + nome.toUpperCase());
		}
		
		if(sexo.toUpperCase().equals("I") ) {
			System.out.println("Bem-vinde " + nome.toUpperCase());
		}

	}

}
